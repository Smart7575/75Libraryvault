export const translateStatus = (status: string, language: Language): string => {
  const mapping: Record<string, Record<Language, string>> = {
    'Ongelezen': { en: 'Unread', nl: 'Ongelezen' },
    'Bezig': { en: 'Reading', nl: 'Bezig' },
    'Gelezen': { en: 'Finished', nl: 'Gelezen' },
    'Wil ik lezen': { en: 'To Read', nl: 'Wil ik lezen' },
  };
  return mapping[status]?.[language] || status;
};

